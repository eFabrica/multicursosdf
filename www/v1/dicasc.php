<strong><em>As crianças não têm noção de&nbsp;perigo.&nbsp;Você que deve protegê-las!<br>
</em></strong><br>
<p><strong></strong></p>




<p><strong>ATROPELAMENTO</strong></p>
<p>Todo cuidado é pouco quando a criança estiver brincando com uma bola: ela pode correr em direção da rua distraidamente para buscá-la. Ensine-a a tomar os devidos cuidados, como olhar para os dois lados para verificar não há nenhum veículo em movimento. </p>
<p><strong></strong></p>
<p><strong>QUEDA</strong></p>
<p>Quando for trocar fraldas ou roupas, jamais deixe a criança desacompanhada sobre móveis, cama, sofá, etc. Somente o berço ou o cercadinho podem ser considerados seguros. </p>
<p>As escadas devem permanecer cercadas e as portas travadas com um peso ou presilha. <br>
<strong></strong></p>
<p><strong>AUTOMÓVEL</strong></p>
<p>Ao andar de automóvel, use a cadeirinha infantil apropriada para a idade/peso. Nunca deixe a criança no banco dianteiro e, caso seja realmente necessário levá-la no colo, sente-se sempre no banco traseiro. </p>
<p>Em viagens prolongadas, não deixe de oferecer líquidos a criança. <br>
<strong></strong></p>
<p><strong>AFOGAMENTO</strong></p>
<p>Não permita que a criança brinque sozinha no banheiro ou próxima de objetos que contenham líquidos, como baldes, bacias e até mesmo a banheira, pois o afogamento não é o único problema: um simples mergulho com aspiração de água até os pulmões pode provocar seqüelas. </p>
<p>Em balneários, praias e piscinas, jamais deixe criança sozinha. Para&nbsp;quem não&nbsp;souber nadar, a água não deve ultrapassar a linha do umbigo.</p>
<p><strong></strong></p>
<p><strong>QUEIMADURA</strong></p>
<p>Verifique a temperatura da água antes de colocar um bebê no banho. A pele é mais sensível do que a dos adultos. </p>
<p>Não permita que criança fique próxima de churrasqueiras, fornos e ferro de passar roupa quente. Ao cozinhar, mantenha as panelas com os cabos para o interior do fogão e, quando tiver que sair de perto, não utilize as chamas externas, pois, a criança pode balançar o fogão e causar um grave acidente.</p>
<p>Criança não deve brincar com fósforos, líquidos inflamáveis, velas ou outros objetos que possam provocar fogo. Nunca deixe uma criança sozinhas trancadas em casa.<br>
</p>
<strong></strong>
<p><strong>INTOXICAÇÃO</strong></p>
<p>Remédios, produtos químicos, de limpeza e venenos devem ser colocados fora do alcance de criança. De preferência em um armário elevado e trancado. Em caso de ingestão, procure imediatamente um médico levando junto o frasco do produto ingerido. <br>
<strong></strong></p>
<p><strong>E AINDA...</strong></p>
<p>Não deixe ao alcance de criança objetos pesados, cortantes ou pontiagudos, como tesoura, agulha e&nbsp;facas. Objetos pequenos, como moedas ou botões devem ser guardados em locais seguros, pois são fáceis de serem engolidos. <br>
</p>
<p>Jamais&nbsp;deixe ao alcance de criança sacos plásticos ou impermeáveis, pois a criança pode colocá-los na cabeça e sufocar-se.</p>