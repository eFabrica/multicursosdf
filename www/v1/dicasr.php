<p><strong>GLP - GÁS DE COZINHA</strong></p>


<p>Ao sentir cheiro de gás, não acenda as luzes.</p>
<p>Abra imediatamente todas as portas e janelas para ventilar o ambiente.</p>
<p>Procure o local de vazamento, passando espuma de sabão. <strong>Nunca </strong>procure vazamentos com um fósforo. </p>
<p><br>
<strong>PONTAS DE CIGARROS</strong></p>
<p>Não jogue pontas cigarros ou fósforos no assoalho, cestos de papéis, jardins ou pela janela. Evite fumar na cama, principalmente se estiver sonolento ou antes de dormir. <br>
</p>
<p><strong>FERRO ELÉTRICO</strong></p>
<p>Sempre desligue o ferro ao afastar-se do local onde estiver passando roupas. Jamais deixe com a base ainda quente sobre tecidos.</p>
<p>&nbsp;</p>
<p><strong>APARELHOS ELÉTRICOS</strong></p>
<p>Ao sair de casa, desligue os aparelhos elétricos, como televisão e ventilador. Se for viajar, tire-os da tomada.</p>