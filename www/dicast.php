<p><strong>Banhistas:</strong></p>




<p>Só tome banho em local seguro, de preferência que você conhece; <br>
<br>
Evite mergulho profundo e jamais mergulhe sozinho; <br>
<br>
Nunca entre na água depois de comer ou beber muito; <br>
<br>
Respeite a sinalização do Corpo de Bombeiros; <br>
<br>
Não vá além das áreas recomendadas para banho; <br>
<br>
Pare quando a água atingir a linha do umbigo; <br>
<br>
Evite rios com correnteza e pedras; <br>
<br>
Para evitar ataque de arraias, não fique em locais isolados; <br>
<br>
Use protetor solar, boné&nbsp;e beba muito liquido; <br>
<br>
Se alguém estiver se afogando, chame o Corpo de Bombeiros.</p>
<p>Só entre na água pra salvar alguém se souber nadar, tiver resistência física e conhecer o local. O ideal é que o socorro seja feito por duas pessoas experientes; <br>
<br>
Mantenha as crianças próximas, de preferência com alguma identificação. Jamais perca os filhos de vista; </p>
<p>&nbsp;</p>
<p><strong>Embarcações:<br>
</strong><br>
As embarcações só podem ser pilotadas por maiores de 18 anos, com carteira de habilitação fornecida pela Marinha; <br>
<br>
As embarcações devem estar em perfeitas condições de uso: sempre com o motor revisado e o casco intacto; <br>
<br>
Para as embarcações, o celular é item de segurança indispensável, mas deve ser protegido da água; <br>
<br>
Os usuários de jet ski também devem respeitar a sinalização existente, principalmente a área reservada aos banhistas; <br>
<br>
O uso de coletes é obrigatório em todas as embarcações para&nbsp;tripulantes e passageiros, inclusive para os usuários de jet ski. <br>
<br>
Os passageiros de lanchas e barcos devem usar roupas adequadas (leves), pois em caso de acidente há maior facilidade para se locomover. Essa dica também vale para os pescadores.</p>
<p>&nbsp;</p>
<p><strong>Acampamentos:<br>
</strong><br>
Evite fazer fogueiras.</p>
<p>Coloque o lixo em sacos plásticos e quando for embora leve com você.</p>
<p>Evite locais onde animais peçonhentos costumam se esconder.</p>
<p>Jamais esteja sozinho. </p>