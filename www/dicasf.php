
<strong>PREVENÇÃO <br>
</strong><br>
Jamais jogue tocos de cigarros acessos às margens de estradas ou rodovias; <br>
<br>
Evite utilizar o fogo em áreas agrícolas, inclusive para o melhoramento de pastagens ou para limpar a terra para o plantio; <br>
<br>
Se for inevitável o uso do fogo, faça apenas a queimada controlada, dentro das recomendações dos órgãos ambientais. Além de ser legal, ela pode evitar o surgimento de um incêndio florestal; <br>
<br>
Mesmo que não vá colocar fogo em sua propriedade, faça aceiros em sua volta, para evitar que o fogo de outras áreas chegue até ela. Os aceiros devem ser feitos de acordo com a vegetação existente: mais largos em casos de matas densas ou árvores com copas; <br>
<br>
<strong>COMBATE <br>
</strong><br>
Para evitar que o fogo se propague, é importante que o combate seja iniciado assim que o foco for detectado. Neste caso, a atuação das brigadas civis ou vinculadas às defesas civis municipais é muito importante. <br>
<br>
Equipamentos agrícolas, como máquinas, enxadas, enxadões e motosseras podem ser usados para abertura de aceiros. <br>
<br>
Uma sugestão é que os fazendeiros viabilizem cursos de capacitação de combate a incêndio para os próprios funcionários. A <strong>Multicursos</strong> oferece esse treinamento para empresas privadas.